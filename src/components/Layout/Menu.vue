const router = useRouter()
const appStore = useAppStore()
const { isMenuCollapsed } = storeToRefs(appStore)
const menuActiveKey = ref(router.currentRoute.value.path ?? '/')

const menuOptions = computed(() => [
  {
    type: 'group',
    label: '我的图片',
    key: 'mian',
    children: [],
    show: !isMenuCollapsed.value,
  },
  {
    label: () =>
      h(RouterLink, {
        to: {
          name: 'Home',
        },
      }, { default: () => '上传图片' }),
    key: '/',
    icon: renderIcon('i-ph-upload-simple-bold w20px h20px'),
  },
  {
    label: () =>
      h(RouterLink, {
        to: {
          name: '/Images/',
        },
      }, { default: () => '图片列表' }),
    key: '/Images',
    icon: renderIcon('i-ph-list-bullets-bold w20px h20px'),
  },
  {
    key: 'divider-1',
    type: 'divider',
  },
  {
    label: () =>
      h('div', { class: 'text-neutral-500 text-xs flex justify-between items-center pr5' }, [
        h('span', { class: '' }, ['存储程序设置']),
      ]),
    key: 'user-storage',
    show: !isMenuCollapsed.value,
    children: selectProgramsOptions.map((item) => {
      return {
        label: () =>
          h(RouterLink, {
            to: {
              path: `/Setting/${item.value}`,
            },
          }, { default: () => item.label }),
        key: `/Setting/${item.value}`,
        icon: renderIcon(item?.icon || 'i-ph-hard-drives-bold w18px h18px'),
      }
    }),
  },
