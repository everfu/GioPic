  router.push(value)
}
</script>

<template>
  <n-menu
    v-model:value="menuActiveKey"
    :options="menuOptions"
    :collapsed="isMenuCollapsed"
    :collapsed-width="64"
    :collapsed-icon-size="22"
    :default-expanded-keys="['user-storage']"
    :indent="22"
    @update:value="updateValue"
  />
</template>

