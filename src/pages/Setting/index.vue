<script setup lang="ts">
import { NButton, NSelect, NSwitch, useOsTheme } from 'naive-ui'
import Keycut from '~/components/Common/Keycut.vue'
import type { TabOption } from '~/types'

const appStore = useAppStore()
const {
  appCloseTip,
  appCloseType,
  autoStart,
  autoUpdate,
  isDevToolsEnabled,
  themeType,
  themeAuto,
} = storeToRefs(appStore)

const osThemeRef = useOsTheme()

const setTabsVal = ref('setTab1')
const isUserScroll = ref(false)

const tabsOptions = ref<TabOption[]>([
  {
    title: '常规',
    items: [
      {
        name: '深浅模式',
        component: () => h(NSelect, {
          value: themeType.value,
          onUpdateValue: (val: 'dark' | 'light') => {
            themeAuto.value = false
            themeType.value = val
          },
          options: [
            { label: '浅色模式', value: 'light' },
            { label: '深色模式', value: 'dark' },
          ],
        }),
      },
      {
        name: '深浅模式是否跟随系统',
        component: () => h(NSwitch, {
          value: themeAuto.value,
          round: false,
          onUpdateValue: (val: boolean) => {
            themeAuto.value = val
            if (val)
              themeType.value = osThemeRef.value
          },
        }),
      },
    ],
  },
  {
    title: '系统',
    items: [
      {
        name: '是否开机自启动',
        component: () => h(NSwitch, {
          value: autoStart.value,
          round: false,
          onUpdateValue: (val: boolean) => {
            autoStart.value = val
            window.ipcRenderer.invoke('auto-start', val)
          },
        }),
      },
      {
        name: '关闭程序时',
        component: () => h(NSelect, {
          value: appCloseType.value,
          disabled: appCloseTip.value,
          onUpdateValue: (val: 'hide' | 'close') => appCloseType.value = val,
          options: [
            { label: '最小化到任务栏', value: 'hide' },
            { label: '直接退出', value: 'close' },
          ],
        }),
      },
      {
        name: '每次关闭程序时都询问',
        component: () => h(NSwitch, {
          value: appCloseTip.value,
          round: false,
          onUpdateValue: (val: boolean) => appCloseTip.value = val,
        }),
      },
      {
        name: '自动检测更新',
        tip: '在启动时检测是否有新版本',
        component: () => h(NSwitch, {
          value: autoUpdate.value,
          round: false,
          onUpdateValue: (val: boolean) => autoUpdate.value = val,
        }),
      },
    ],
  },
  {
    title: '其他',
    items: [
      {
        name: '开发者工具',
        tip: () => h('span', ['开启后可使用', h(Keycut, { ctrl: true, shift: true }, () => 'D'), '打开开发者工具']),
        component: () => h(NSwitch, {
          value: isDevToolsEnabled.value,
          round: false,
          onUpdateValue: (val: boolean) => {
            isDevToolsEnabled.value = val
            window.ipcRenderer.invoke('reg-dev-tools', val)
          },
        }),
      },
      {
        name: '程序重置',
        tip: '若程序显示异常或出现问题时可尝试此操作',
        component: () => h(NButton, {
          type: 'error',
          strong: true,
          secondary: true,
          onClick: () => {
            window.$dialog.warning({
              title: '重置',
              content: '重置所有设置，是否继续？',
              positiveText: '确定',
              negativeText: '取消',
              autoFocus: false,
              onPositiveClick: async () => {
                await appStore.resetState()
                // 显示重置成功提示
                window.$message.success('重置成功，部分设置在重启后生效')
              },
            })
          },
        }, () => '重置'),
      },
    ],
  },
])

// 切换Tab
function setTabChange(name: string) {
  isUserScroll.value = true // 用户主动切换Tab时设置标志位
  nextTick(() => {
    const index = Number(name.replace('setTab', '')) - 1
    const setEl = document.querySelectorAll('.set-type')[index] as HTMLElement
    if (!setEl)
      return
    setEl.scrollIntoView({
      behavior: 'smooth',
    })
  })
}

// 滚动监听
const allSetScroll = debounce((e) => {
  if (isUserScroll.value) {
    isUserScroll.value = false // 重置标志位
    return
  }

  const distance = e.target.scrollTop + 30
  const allSetDom = document.querySelectorAll('.set-type')
  allSetDom.forEach((v, i) => {
    const el = v as HTMLElement
    if (distance >= el.offsetTop)
      setTabsVal.value = `setTab${i + 1}`
  })

  const isReachEnd = e.target.scrollHeight - e.target.scrollTop <= e.target.clientHeight + 30

  if (isReachEnd) {
    setTabsVal.value = `setTab${allSetDom.length}`
  }
}, 100)
</script>

<template>
  <div @scroll="allSetScroll">
    <div class="select-none text-4xl font-600">
      程序设置
    </div>
    <n-tabs
      v-model:value="setTabsVal"
      class="tab-bgmask sticky top-0 z-1 mt4 py4"
      type="segment"
      @update:value="setTabChange"
    >
      <n-tab
        v-for="(tab, index) in tabsOptions"
        :key="tab.title"
        :name="`setTab${index + 1}`"
        :tab="tab.title"
      />
    </n-tabs>

    <SettingSection
      v-for="tab in tabsOptions"
      :key="tab.title"
      class="scroll-mt-10"
      :title="tab.title"
      :items="tab.items"
    />
  </div>
</template>

<style scoped>
.tab-bgmask {
  background-image: linear-gradient(var(--n-color) 80%, transparent);
}
</style>
